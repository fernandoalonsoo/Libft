#include "../libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*aux;

	if (!lst || !del)
		return ;
	while (*lst)
	{
		aux = *lst;
		del(aux -> content);
		*lst = aux -> next;
		free(aux);
	}
}

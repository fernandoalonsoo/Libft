#include "../libft.h"

int	ft_lstsize(t_list *lst)
{
	t_list	*p;
	size_t	i;

	p = lst;
	i = 0;
	if (!p)
		return (0);
	while (p -> next != NULL)
	{
		p = p -> next;
		i++;
	}
	return (i);
}
